	public static char[] decodeHex(char[] data) {
		int len = data.length;
		if ((len & 0x01) != 0) {
			throw new RuntimeException("Odd number of characters.");
		}
		char[] out = new char[len >> 1];
		// two characters form the hex value.
		for (int i = 0, j = 0; j < len; i++) {
			int f = toDigit(data[j], j) << 4;
			j++;
			f = f | toDigit(data[j], j);
			j++;
			out[i] = (char) (f & 0xFF);
		}
		return out;
	}

	/**
	 * 将十六进制字符转换成一个整数
	 * @param ch
	 *            十六进制char
	 * @param index
	 *            十六进制字符在字符数组中的位置
	 * @return 一个整数
	 * @throws RuntimeException
	 *             当ch不是一个合法的十六进制字符时，抛出运行时异常
	 */
	protected static int toDigit(char ch, int index) {
		int digit = Character.digit(ch, 16);
		if (digit == -1) {
			throw new RuntimeException("Illegal hexadecimal character " + ch
					+ " at index " + index);
		}
		return digit;
	}


	public static byte getByte(int intValue){
		byte byteValue=0;
		int temp = intValue % 256;
		if ( intValue < 0) {
			byteValue =  (byte)(temp < -128 ? 256 + temp : temp);
		}
		else {
			byteValue =  (byte)(temp > 127 ? temp - 256 : temp);
		}
		return byteValue;
	}

	public static byte[] getBytes (char[] chars) {

		byte buf[] = new byte[chars.length];

		for(int i = 0; i < chars.length ; i ++){
			buf[i] = getByte(chars[i]);
		}
		return buf;
	}

	public static char[] getChars (byte[] bytes) {
		Charset cs = Charset.forName ("UTF-8");
		ByteBuffer bb = ByteBuffer.allocate (bytes.length);
		bb.put (bytes);
		bb.flip ();
		CharBuffer cb = cs.decode (bb);

		return cb.array();
	}
